a = 5
print("a =",a)
# penambahan
print("\n==== penambahan ==== \n")
a += 5
print("a =",a)

# pengurangan
print("\n==== pengurangan ==== \n")
a -= 2
print("a =",a)

# perkalian
print("\n==== perkalian ==== \n")
a *= 2
print("a =",a)

# pembagian
print("\n==== pembagian ==== \n")
a /= 2
print("a =",a)

# perpangkatan
print("\n==== perpangkatan ==== \n")
a **= 2 
print("a =",a)

# modulo
print("\n==== modulo ==== \n")
a %= 3 
print("a =",a)

# floor division
print("\n==== floor division ==== \n")
a = 5
a //= 3 
print("a =",a)

# bitwise OR
print("\n==== bitwise OR ==== \n")
a = True
print("nilai a =",a)
a |= False
print("nilai a | false =",a)

# bitwise AND
print("\n==== bitwise AND ==== \n")
a = True
print("nilai a =",a)
a &= False
print("nilai a & false =",a)

# bitwise XOR
print("\n==== bitwise XOR ==== \n")
a = True
